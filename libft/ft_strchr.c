#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *t;

	t = (char *)s;
	while (t && *t)
	{
		if (c == *t)
			return (t);
		t++;
	}
	if (!*t && c)
		return (0);
	return (t);
}
