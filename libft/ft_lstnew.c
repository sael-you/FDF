#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	if (!content)
	{
		new->content = NULL;
		new->content_size = 0;
	}
	else
	{
		if (!(new->content = ft_strdup((char *)content)))
		{
			free(new);
			return (NULL);
		}
		new->content_size = content_size;
	}
	new->next = NULL;
	return (new);
}
